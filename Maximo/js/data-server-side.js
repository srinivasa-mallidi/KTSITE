function loadServerTopic(topic) {

  document.querySelectorAll(".inner-nav-btn")
    .forEach(btn => btn.classList.remove("active"));

  event.target.classList.add("active");

  const container = document.getElementById("server-topic-content");

  switch(topic) {
    case "spn":
      container.innerHTML = getSPNContent();
      break;
    case "cert":
      container.innerHTML = getCertContent();
      break;
    case "tools":
      container.innerHTML = getToolsContent();
      break;
    case "bundles":
      container.innerHTML = getBundleContent();
      break;
    case "aro":
      container.innerHTML = getAROContent();
      break;
  }
}

